		<h2>Legs Asleep</h2>
		<p class="years">Written August 2010</p>
		<p>My school had an open floor plan where large spaces were used for three class groups studying the same subject at the same time separated by rolling partitions. Sometimes, these rolling partitions were pushed aside and the separate classes would come together for some activity. In addition to the unique usage of the floor plan, Language Arts and Social Studies classes were taught as a back-to-back class period block. So these multiple classes stayed in the large shared space for two periods at a time.</p>
		<p>One day, the three Language Arts and Social Studies classes were brought together to gather around and watch some movie (I don't remember what it was that we watched) that related to the topic we'd been covering and lasted through both class periods. The teachers had us students sit on the floor in front of the rolling t.v. cart to watch it.</p>
		<p>An hour and forty-five minutes went by while sitting cross-legged on the floor watching this movie.  Then, the bell rang. My classmates got up, gathered their things and started heading to their next class. I tried to get up but, as I started to move, I felt oddly numb.</p>
		<p>Both my legs had fallen asleep, from the hips right down to my toes. Somehow, I managed to get up despite the creepy, crawly pins and needles sensation that began to overcome my lower extremities. I almost fell down as I bent to grab my book bag. As I slowly moved down the hall, I felt like Frankenstein's monster taking his first awkward steps. Kids that were passing in the opposite direction clearly noticed the odd grimaces that I was unsuccessfully trying to suppress.</p>
		<p>My next class was all the way at the other end of the school, and I didn't think I was going to make it on time. Luckily, I ran into a couple of friends who laughed when I told them what was going on, but helped me along until the circulation in my legs returned and I could walk normally again.</p>
		<p>I did make it to my next class on time, though just barely.  But to this day, if I have to sit in one place for any length of time, I'm always sure to shift around and change my position occasionally so that my legs don't fall asleep like that time in junior high.</p>
