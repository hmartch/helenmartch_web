		<h2>Going To Gringott's</h2>
		<p class="years">Written October 2010</p>

<p>As a Muggle-born, I hadn't heard of Gringott's Bank until about a year ago,
when I found out that I was a witch and was to attend Hogwarts School of
Witchcraft and Wizardry. My parents knew about Hogwarts and the wizarding world
because, it turns out, my mother has a cousin that went to Hogwarts. Though
they'd had a few suspicions about my magic abilities because of a few things
that happened when I was little, Mom and Dad never told me anything before the
letter came, because Mom didn't want me to get my hopes up if a letter from
Hogwarts never arrived.</p>

<p>Anyway, when my letter did come, Mom invited over her cousin, June, so she
could answer all the questions that I had and so she could take me to Diagon
Alley and to Gringott's for the first time. June told me about how Gringott's
was run by goblins and I was rather shocked for I'd only known of goblins from
fairy tales in which they were usually quite nasty. So, I must admit that I was
rather frightened, though fascinated, on that first visit.</p>

<p>Cousin June led the way as we entered the bank, giving me a moment to pause
and marvel at the uniformed goblin guards, the large bronze and then silver
doors at the entrance. She'd already told me of the warning poem, but I stopped
to read it anyway. Although I had no intention of ever attempting to steal from
Gringott's, reading the poem gave me an oddly guilty feeling.</p>

<p>When we stepped into the main hall, I was rather overwhelmed by the size of
the marbled place with all its doors and all the goblins everywhere. While I
was gawking at the place, June spoke with a goblin who directed her to a
particular open spot at the counter. She had to grab me by the elbow to get my
attention to follow along.</p>

<p>June placed a key on the counter while she addressed the goblin sitting
behind it. "Good morning. I should like to visit my vault, number 323. And we
need to contract for a new vault, just a small one, for my young cousin here
who will be starting at Hogwarts soon."</p>

<p>Without a word, the old bespeckled goblin behind the counter rose a bit from
his seat and peered at me from over his glasses. It made me somewhat
uncomfortable but he didn't seem to find any faults to speak of for he just
gave a curt nod, sat back down, and pulled out some paperwork. He took down my
name and address, noted the year I was beginning my studies at Hogwarts, and
had me sign the form. June signed as well, both as witness to my signature as
well as to give testimony that I was indeed a witch and deserving of a
Gringott's vault.</p>

<p>The old goblin handed me a key and said, "Vault number 189." Then he pointed
towards a door which had just opened behind him. "That way."</p>

<p>We thanked the old goblin and headed towards the door he'd indicated. As we
reached it, a second, seemingly younger (though it was a bit hard to tell)
goblin stepped in front of us. "Keys, please," he said, extending a gnarled
hand with very long fingernails. Satisfied, he gestured for us to follow him
into the stone passageway behind the door. I followed June onto the small cart
that the goblin lead us to and watched while he climbed in and took the
controls. I must have looked nervous because June said to me, "Don't worry.
It's a bit fast, like a roller coaster, but if you're relaxed it's fun!"</p>

<p>Indeed it did get going fast, I could feel my hair whipping around my face on
the way to Cousin June's vault. I tried to look down other passageways that we
passed on the way, but we were going to fast to see anything. I could tell that
we were heading down, but otherwise with the twists and turns of the track I
couldn't really say which way we'd gone. The cart slowed down, then stopped and
the goblin said, "Vault 323." I saw a strong looking door with criss-crossing
metals straps for reinforcement and two keyholes. The goblin took June's key
and another key, must have been a master key the goblins use, and turned both
at once. The door opened, June stepped inside and then reemerged a moment
later. She and the goblin got back into the cart.</p>

<p>As the goblin started the cart, June said to me, "We'll go visit your vault
now." I started to ask why, since I had no wizard money to deposit, but the cart
was already going so fast my voice was drowned out.</p>

<p>The trip to my new vault was heading back up, closer to ground level, but
otherwise the experience was more of the same twisting and turning. When we
arrived at vault number 189, I was able to ask my question. "Why would we
bother coming to my vault when I don't have any wizard money yet?  I only have
the Muggle money that Mom and Dad gave me."</p>

<p>Cousin June smiled at me and answered, "Goblins have their own special magic
for doing currency exchange. Go ahead and pull out your Muggle money and give
it to our friend here."</p>

<p>While we were speaking, the goblin had already opened my vault with the same
two key arrangement as on June's vault. My vault, however, was more the size of
a kitchen cabinet, rather than the walk-in size that June had.</p>

<p>I opened my purse, pulled out my Muggle money and handed it to the goblin.
Without counting it, he set it inside my vault then closed the door. I looked
at June, puzzled. She smiled and nodded sideways to wordlessly say "keep
watching." The goblin then re-opened the vault and, where the pile of my Muggle
bills had been was now a small pile of Wizard coins. June explained, "It's the
same value as your Muggle money had been but now in the wizard currencies of
galleons, sickles and knuts." Then June reached into her purse, pulled out a
small pouch and handed it to me.</p>

<p>I looked at her in surprise and she said, "A little present."</p>

<p>Pulling the pouch open I saw that it contained nearly as many wizard coins
as were already sitting in my new vault. "Wow! Thank you!" I said to June with
a smile.</p>

<p>She said, "Welcome to the wizarding world. It's nice to have another witch
in the family. Now let's finish up here and go have an ice cream treat at
Florean Fortescue's before we start shopping for your school things. Oh, and no
need to mention this little present to your folks. It can be our little
secret&hellip; just between witches."</p>

<p>I put most of the money from June's present into the vault, keeping some out
for the shopping we'd be doing, and then the goblin locked the vault and we all
climbed back into the cart.</p>

<p>After another brief ride back to the passageway to the main hall, the goblin
returned to us our vault keys. We thanked him and walked back out through the main
hall with all its activity, out the big shining doors at the entrance and back
out into Diagon Alley.</p>


