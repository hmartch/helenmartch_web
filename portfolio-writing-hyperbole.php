		<h2>Excruciating Pain</h2>
		<p class="years">Written May 2010</p>
		<p>It happened as I was driving home from work one day. I was only about two or three blocks from my house, waiting at an intersection with my turn signal on. After the oncoming traffic had passed, I glanced one last time to my left to make sure there were no pedestrians crossing the street as I began to make my turn. Suddenly, it felt as though I was being stabbed with a razor-sharp implement. My eyes shuttered closed involuntarily. I struggled to keep them open despite the excruciating pain. I needed to keep my eyes on the road.</p>
		<p>I completed the turn with my hands white-knuckled from holding the wheel so hard. Once I was driving straight again, I kept steering with just my right hand as my left hand flew up to my face trying in vain to find some way to relieve the pain that was now radiating through my face. My left eye felt like it was on fire, like a hot, sharp poker straight out of a fire was now rammed into it. I moaned as I tried to both keep watch on the road and look at myself in the rear view mirror.  I couldn't do it. I gritted my teeth and willed myself to make it home.</p>
		<p>An eternity later, I pulled into my garage.  I threw the car into park and scrambled for the rearview mirror. I could see that my eye was scarlet red and swelling fast but the light was too dim in the garage to be able to see much more. Every blink of my eyes was unbearable. Hot tears welled up in my eyes and streaked down my face. I had to get inside and deal with this, now, or I could be blinded for life.</p>
		<p>After slamming the door closed behind me, I dropped my things to the floor and stumbled blindly to the bathroom. The strongest light and the best mirror in the house were in the bathroom. With shaking hands, I fumbled for the light switch. I took one look at myself and gasped. The glimpse of myself in the car's rearview mirror had not prepared me for this. My eyelid was so swollen that I looked like a boxer on the losing side of a heavyweight bout. I pried my eyelid open to get a look at the eye itself. My eyeball was so bloodshot it looked as though it was filled with rivulets of molten lava.</p>
		<p>The initial sensation of being stabbed in the eye was still present but it was now accompanied by the feeling of the sands of Sahara being ground into my eye.  I pulled up my upper eyelid. I pulled down my lower eyelid. I searched and searched for the cause of this unbelievable pain but I couldn't find the culprit. I grabbed the cup that I use when I brush my teeth and filled it with water. Then I leaned over the sink and poured the whole cup into my eye while I held my eyelid open with my trembling fingers.</p>
		<p>I arose, spluttering, and stared at myself in the mirror. I noticed something dark in the inner corner of my eye. Gently I probed my tender eye with my index finger. I looked down and there on the tip of my finger was the source of all my pain and agony&hellip; an eyelash.</p>
