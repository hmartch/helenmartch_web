		<h2>His Heart's Desire</h2>
		<p class="years">Written April 2010</p>
		<p>It was the one thing he coveted most in the world. Try though he might to resist the pull of obsession, he knew it was futile. Every day's thoughts and every night's dreams were filled with images of his heart's desire. He would never be able to do what he most wished &mdash; to hold her in his arms and tenderly kiss her lips. Somehow he must find a way to be content with what he had, for he was flesh and blood and she was merely the stunningly beautiful subject of the richly framed painting on his wall.</p>

<p>When he first saw her painting, he'd been drawn to it with an intensity that had taken him by surprise. He was in London on a business trip and, after a disastrous meeting that he was sure would get him fired, he was wandering through the shopping district looking into windows and trying to clear his head. Suddenly, it started to rain. It was not just a light sprinkle. It was the kind of rain in which he'd be drenched to the skin in seconds. He ducked into the nearest shop doorway for shelter.</p>

<p>Once inside, he looked around. Antiques and junk of all sorts cluttered the old shop. Dusty books and knick-knacks covered tables. Bottles and jars with unidentifiable contents filled shelves along the walls. In one corner near the door stood a mannequin wearing a lop-sided top hat and tuxedo with a cape. Beside it there seemed to be an umbrella stand. He squeezed past an over-stuffed armchair to take a closer look.</p>

<p>Out of the corner of his eye, he thought he saw movement at the back of the store. He turned, preparing to indulge in some small talk with the shop keeper. He didn't see anyone, however; he saw the back wall of the shop, covered with paintings. They were landscapes and still-lifes, mostly, but the painting in the center was a portrait so crisp and clear that it could have been a photograph.</p>

<p>A lovely woman with a single, long braid of chestnut hair draped over her right shoulder looked out at him from inside the carved wooden frame. She wore a simply cut, rose-colored gown and her hands were clasped loosely in front of her. At her throat was a string of lacquered beads with a distinctive pendant which hung just above the V of her gown's neckline. The smooth skin of her heart-shaped face had a gentle glow but her most striking feature were her sparkling violet eyes. He was entranced.</p>

<p>Completely forgetting about the umbrella stand and the driving rain outside, he moved towards the painting until he was directly in front of it, never taking his eyes away once he saw her. He stood in front of the portrait, continuing to stare at it, absorbing every detail. He wondered who she was and how the portrait could seem so real. Though he knew it was impossible, he felt that if he watched long enough, eventually she'd move or speak.</p>

<p>"Lovely, isn't she?" said a voice.</p>

<p>Startled, he turned and saw an elderly woman seated behind a counter at the far side of the shop.  The old woman rose and came out from behind the counter. She spoke as she walked over to join him in front of the paintings.</p>

<p>"You've got a good eye, sir. This portrait is by far the prettiest item in my shop. The bloke I bought it from said he was torn up to have to sell it but he needed the money bad. He was an odd one, that's for sure... wore strange clothes and kept mumbling things that didn't make any sense," she shook her head and waved her hand as if shooing a fly, "but all that doesn't matter.  Are you interested in purchasing it, sir?"</p>

<p>It hadn't occurred to him that he could buy the portrait but, when asked, he immediately said, "Yes."</p>

<p>A short time later, he left the store with a hastily selected umbrella in one hand and the painting, triply wrapped in plastic for protection, tucked under his other arm.  He hated to see the beautiful woman covered up but he was afraid to see the portrait damaged.</p>

<p>Back in his hotel room, he gently unwrapped the plastic coverings and propped the painting on the arms of a chair in the sitting area. He ordered room service for dinner so he would not have to leave the room, and the portrait, in order to eat.  He sat gazing at the portrait late into the night until finally he fell asleep.</p>

<p>In the morning he checked out of the hotel and headed home with his treasured portrait. He knew just where to hang the portrait in his little house &mdash; on the wall opposite the window that looked out onto the garden.</p>

<p>"You'll like it here," he said later as he hung the portrait, "it is the sunniest spot in the house and you can watch the flowers bloom." It didn't occur to him that it was a bit odd to be speaking to a painting.</p>

<p>Luckily, he was not fired after the disastrous meeting in London, but his boss was not pleased and had him work long hours to make things right. Even after the longest, hardest day, however, he returned home and looked at the lovely woman in the painting.</p>

<p>"It doesn't matter," he said to her, "as long as you're here."</p>

<p>He would look at the painting whenever he could during the day and he'd dream about it at night so much that he began to have trouble telling the difference. So when, late one night, a strange thing happened, it didn't seem strange to him at all.</p>

<p>As he sat gazing dreamily at her portrait, the woman in the painting spoke, "You do love me, don't you?"</p>

<p>"Indeed, I do, my darling."</p>

<p>"And, if I asked something of you, you would do it. Wouldn't you?"</p>

<p>"There's nothing that I wouldn't do for you," he said to her.</p>

<p>She said, "I'd like you to return me to my family." The woman in the portrait told him an address. She said, "Write to them. Tell them you're bringing me then take me there as soon as you can."</p>

<p>The sun woke him as it streamed through the window. He looked at the portrait but she was still. With a heavy heart, for he did not want to lose the painting but he was sincerely willing to do anything for her, he wrote to the address she had given him. In the letter, he described the portrait and said that he would bring it the following weekend. For the rest of the week, he went through his normal routines. The portrait woman did not speak to him during the day or even during his dreams at night.</p>

<p>Saturday, it was raining. He carefully wrapped the painting, loaded it into his car, and set out on the drive into the country. He found the address and drove up the long drive to the small, but stately, manor home. With the portrait and an umbrella in hand, he rang the front door bell.</p>

<p>A woman answered the door. He introduced himself and, raising the painting slightly, said, "I wrote that I'd be coming."</p>

<p>"Yes, of course. Welcome! Please come in," said the woman as she stepped aside to allow him room to enter. She graciously took his wet umbrella and hung it on a coat rack beside the door. "Do make yourself comfortable in the drawing room while I go fetch my husband." She indicated a nearby doorway before she headed down the hall towards the back of the house.</p>

<p>When he entered the drawing room, he saw a young woman seated with her head bowed over a book. She had long, flowing, chestnut hair and was wearing a pale blue top. Hearing him enter, she lifted her head and he could see her features more clearly but he could scarcely believe what he saw. The smooth skin of her heart shaped face framed the same violet eyes he had been staring at for weeks. And around her throat were the lacquered beads with the distinctive pendant. He couldn't speak from the feeling of his breath leaving him.</p>

<p>Rising from her seat and gesturing towards the wrapped package in his arms, she said, "Ah. Hello. You must be the gentleman who found Grandmother's portrait. Father mentioned you'd be coming."</p>

<p>Stunned, he slowly opened his mouth, about to say, "It's you," when the young woman's parents entered the room.</p>

<p>"Hello, hello. I was quite surprised and pleased to get your letter," said the father as he came in. "Wasn't sure we were ever going to see Mum's portrait again after that scoundrel made off with it. Where did you find it?"</p>

<p>"At a shop in London, sir."</p>

<p>The father didn't seem to really be too concerned about how the painting had changed hands though. He reached out and took the painting. "Let's get these wrappings off and have a look at it."</p>

<p>As the father unwrapped the portrait, the young woman who looked so much like her grandmother came over and put her hand on the young man's arm. "Thank you so much for returning the painting of my grandmother. Won't you please join us for tea?"</p>

<p>At her touch, his skin tingled and he looked into her sparkling violet eyes and said with a smile, "Certainly. I would be delighted."</p>

<p>Then he turned to look at the portrait that was now freed of its wrappings and saw that the woman in the painting was smiling too.</p>
